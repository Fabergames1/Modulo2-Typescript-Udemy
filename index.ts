// Declarando e atribuindo um número à variável
let numero: number = 25;

// Convertendo o número para uma string e armazenando em uma nova variável
let numeroString = numero.toString();

// Criando uma string maior usando concatenação
let stringMaior: string = "O número é: " + numeroString;

// Imprimindo a string maior no console
console.log(stringMaior);